    async function deployContract(contractName, abi, bytecode, args) {
        const signer = await getSigner();
        if (!signer) return;

        if (!bytecode) {
            showToast(`Contract not compiled yet. Run 'npx hardhat compile' first, then restart the server.`, 'warning');
            return;
        }

        try {
            showToast(`Deploying ${contractName}... Please confirm in MetaMask.`, 'info');

            const factory = new ethers.ContractFactory(abi, bytecode, signer);
            const contract = await factory.deploy(...args);

            showToast(`Transaction sent! Waiting for confirmation...`, 'info');

            const receipt = await contract.deploymentTransaction().wait();

            // Show result
            const resultDiv = $('#deployResult');
            resultDiv.style.display = '';
            
            const contractAddr = await contract.getAddress();
            $('#deployedAddress').textContent = contractAddr;
            $('#deployTxHash').textContent = receipt.hash;
            $('#deployBlock').textContent = receipt.blockNumber;
            $('#deployGas').textContent = receipt.gasUsed.toLocaleString();

            const network = await signer.provider.getNetwork();
            const networkNames = { 1n: 'Ethereum', 169n: 'Manta Pacific', 3441006n: 'Manta Testnet', 137n: 'Polygon', 56n: 'BSC', 11155111n: 'Sepolia' };
            $('#deployNetwork').textContent = networkNames[network.chainId] || `Chain ${network.chainId}`;

            // Auto-fill interact address
            $('#interactAddress').value = contractAddr;

            showToast(`${contractName} deployed successfully!`, 'success');

        } catch (err) {
            console.error('Deploy error:', err);
            if (err.code === 4001 || err.code === 'ACTION_REJECTED') {
                showToast('Transaction rejected by user', 'warning');
            } else {
                showToast(`Deploy failed: ${err.message?.slice(0, 100)}`, 'error');
            }
        }
    }

